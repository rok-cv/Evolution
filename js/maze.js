function calcWaypoints(resitev) {
  var waypoints = [];
  for (var i = 1; i < resitev.length; i++) {
    var pt0 = resitev[i - 1];
    var pt1 = resitev[i];
    var dx = pt1.x - pt0.x;
    var dy = pt1.y - pt0.y;
    for (var j = 0; j < 100; j++) {
      var x = pt0.x + (dx * j) / 100;
      var y = pt0.y + (dy * j) / 100;
      waypoints.push({ x: x, y: y });
    }
  }
  return waypoints;
}

var points = calcWaypoints(resitev);

var t = 1;

function animation() {
  for (var i = 0; i < 10; i++) animate();
}

function animate() {
  if (t < points.length - 1) {
    requestAnimationFrame(animate);
  }
  ctx.beginPath();
  ctx.moveTo(points[t - 1].x, points[t - 1].y);
  ctx.lineTo(points[t].x, points[t].y);
  ctx.strokeStyle = "#66ff66";
  ctx.lineWidth = 3;
  ctx.stroke();
  t++;
  if (t >= points.length)
    for (var i = 0; i < 10; i++) {
      evol();
    }
}

